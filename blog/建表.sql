create database blog;
go
use blog; --打开数据库
go
--创建文章类别表
create table articleType
(
	id int identity(1,1) primary key,
	t_name varchar(50) not null
)
go
--创建文章表
create table articlelist
(
	id int identity(1,1) primary key,
	aname varchar(100) not null,--作者
	a_title varchar(50),--标题
	a_preface varchar(300),--前言
	img varchar(100),--图片地址
	playtimes int default(0),--点赞个数
	publishtime datetime,--时间
	brief text,--内容
	tid int,--文章类型，对应文章类别表的id
	constraint list_typet_fk foreign key(tid) references articleType(id)
);
--创建相册表
go
create table photo
(
	id int identity(1,1) primary key,
	p_time datetime,--图片时间
	p_title varchar(50),--标题
	brief text,--内容
	img varchar(300),--图片地址，可多个
	commend int default(0),--点赞个数
)
--创建日记表
go
create table diary
(
	id int identity(1,1) primary key,
	d_time datetime,--日记时间
	d_title varchar(100),--日记标题
	d_info text,--日记内容
	playtimes int default(0)--点赞个数
)

--创建留言表
go
create table guestbook
(
	id int identity(1,1) primary key,
	g_time datetime not null,--留言时间
	g_name varchar(100) not null,--留言昵称
	g_img varchar(50) not null,--留言者头像
	g_info varchar(300)--留言内容
)
--创建用户表
go
create table admins
(
	id int identity(1,1) primary key,
	name varchar(100) not null,
	pwd varchar(40) not null,
	limit int default(2)
);
